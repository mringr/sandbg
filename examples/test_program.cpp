#include <iostream>

int main() {
    int a = 10;
    int b = 20;

    int result = a + b;

    std::cerr << "a + b = " << result << "\n";

    std::cerr << "a - b = " << a - b << "\n";

    std::cerr << "a * b = " << a * b << "\n";


    return result;
}